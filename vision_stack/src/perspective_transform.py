if __name__ == "__main__":
    """
    Standalone test harness.

    MISSING DEPENDENCY check:
      If calibration/homography_matrix.npz does not exist, the harness prints
      an explicit error and exits. Do not proceed without calibrated data.

    Input priority (per sample dir):
      1. Pre-cropped *_roi_lane.png from sN/results/ (written by roi_crop.py)
      2. Falls back to full image from sN/ with inline lane ROI crop

    For each lane ROI:
      MODE A (image warp):
        stem_pt_src_points.png       — source frame with calibration quad overlay
        stem_pt_warped.png           — bird's-eye warped image
        stem_pt_warped_dst_pts.png   — warped image with destination quad overlay

      MODE B (point transform):
        Anchor points are the mid-bottom edge of the lane ROI (a simple proxy
        for lane center at the near edge) plus the four corners. In production
        these would be DetectionObject.position centroids from feature_fusion.py.
        stem_pt_points_warped.png    — warped image with transformed points marked

    All outputs → sN/results/

    Coordinate note printed to stdout for each unique resolution encountered.
    """
    import os
    import sys

    SAMPLE_DIRS = [
        "vision_stack/sample_img/duckietown/s1",
        "vision_stack/sample_img/duckietown/s2",
        "vision_stack/sample_img/duckietown/s3",
        "vision_stack/sample_img/duckietown/s4",
        "vision_stack/sample_img/duckietown/s5",
    ]
    HOMOGRAPHY_PATH  = "vision_stack/calibration/homography_matrix.npz"
    IMAGE_EXTENSIONS = (".jpg", ".jpeg", ".png", ".bmp")

    # --- Calibration check --------------------------------------------------
    if not os.path.exists(HOMOGRAPHY_PATH):
        print(
            f"[ERROR] {HOMOGRAPHY_PATH} not found.\n"
            "Homography calibration is required before running this substage.\n"
            "Perform Calibration Step 3 on actual course hardware:\n"
            "  1. Place a flat grid of known dimensions on the course surface.\n"
            "  2. Identify four src/dst point pairs.\n"
            "  3. Compute H with cv2.getPerspectiveTransform().\n"
            "  4. Save to calibration/homography_matrix.npz with keys:\n"
            "     homography_matrix, output_width, output_height,\n"
            "     src_points, dst_points"
        )
        sys.exit(1)

    try:
        homography = load_homography(HOMOGRAPHY_PATH)
        print(
            f"[INFO] Homography loaded from {HOMOGRAPHY_PATH}\n"
            f"       Output canvas: {homography.output_width}x{homography.output_height} px"
        )
    except (KeyError, ValueError) as e:
        print(f"[ERROR] Failed to load homography: {e}")
        sys.exit(1)

    seen_shapes = set()
    total_ok    = 0
    total_fail  = 0

    for sample_dir in SAMPLE_DIRS:
        if not os.path.isdir(sample_dir):
            print(f"[SKIP] Not found: {sample_dir}")
            continue

        results_dir = os.path.join(sample_dir, "results")
        os.makedirs(results_dir, exist_ok=True)

        # Prefer pre-cropped lane ROIs
        lane_files = {}
        if os.path.isdir(results_dir):
            for f in os.listdir(results_dir):
                if f.endswith("_roi_lane.png"):
                    stem = f.replace("_roi_lane.png", "")
                    lane_files[stem] = os.path.join(results_dir, f)

        use_inline = len(lane_files) == 0
        full_imgs = []
        if use_inline:
            full_imgs = sorted(
                f for f in os.listdir(sample_dir)
                if os.path.splitext(f)[1].lower() in IMAGE_EXTENSIONS
            )
            if not full_imgs:
                print(f"[SKIP] No inputs in {sample_dir}")
                continue
            stems = [os.path.splitext(f)[0] for f in full_imgs]
        else:
            stems = sorted(lane_files)

        for stem in stems:
            if use_inline:
                ext  = next(
                    os.path.splitext(f)[1]
                    for f in full_imgs if os.path.splitext(f)[0] == stem
                )
                img  = cv2.imread(os.path.join(sample_dir, f"{stem}{ext}"))
                if img is None:
                    print(f"[FAIL] Could not read: {stem}")
                    total_fail += 1
                    continue
                H, W    = img.shape[:2]
                lane_roi = img[H // 2 : H, 0 : W]
            else:
                lane_roi = cv2.imread(lane_files[stem])
                if lane_roi is None:
                    print(f"[FAIL] Could not read: {lane_files[stem]}")
                    total_fail += 1
                    continue

            H_r, W_r = lane_roi.shape[:2]

            # Print coordinate context once per unique resolution
            if (H_r, W_r) not in seen_shapes:
                seen_shapes.add((H_r, W_r))
                print(f"\n[COORDS] Lane ROI {W_r}x{H_r}  →  "
                      f"Bird's-eye canvas {homography.output_width}x{homography.output_height}")
                print(f"  src_points: {homography.src_points.tolist()}")
                print(f"  dst_points: {homography.dst_points.tolist()}")

            try:
                # --- MODE A: full image warp --------------------------------
                warp_result = warp_lane_roi(lane_roi, homography)

                src_overlay = draw_source_points(lane_roi, homography)
                cv2.imwrite(
                    os.path.join(results_dir, f"{stem}_pt_src_points.png"),
                    src_overlay)

                cv2.imwrite(
                    os.path.join(results_dir, f"{stem}_pt_warped.png"),
                    warp_result.warped_image)

                dst_overlay = draw_destination_points(warp_result.warped_image, homography)
                cv2.imwrite(
                    os.path.join(results_dir, f"{stem}_pt_warped_dst_pts.png"),
                    dst_overlay)

                # --- MODE B: point transform --------------------------------
                # Proxy anchor points: corners + mid-bottom of lane ROI.
                # In production: use DetectionObject.position centroids.
                proxy_points = np.array([
                    [0,       0      ],   # top-left
                    [W_r - 1, 0      ],   # top-right
                    [W_r - 1, H_r - 1],  # bottom-right
                    [0,       H_r - 1],  # bottom-left
                    [W_r // 2, H_r - 1], # mid-bottom (lane center proxy)
                ], dtype=np.float32)

                pt_result = transform_points(proxy_points, homography)

                pts_overlay = draw_transformed_points(
                    warp_result.warped_image, pt_result, label="P")
                cv2.imwrite(
                    os.path.join(results_dir, f"{stem}_pt_points_warped.png"),
                    pts_overlay)

            except (ValueError, TypeError) as e:
                print(f"[FAIL] {stem}: {e}")
                total_fail += 1
                continue

            print(f"[OK] {stem}  lane_roi={W_r}x{H_r}  "
                  f"warped={homography.output_width}x{homography.output_height}")
            total_ok += 1

    print(f"\nDone. {total_ok} processed, {total_fail} failed.")