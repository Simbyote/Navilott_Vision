# Test harness
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    """
    Standalone test harness.

    Loads every .jpg / .png from each of the five sample image directories,
    runs preprocess_frame(), and writes four debug images per input into
    vision_stack/sample_img/duckietown/sN/results/

    MISSING DEPENDENCY: calibration/camera_matrix.npz must exist before
    running. If it does not exist, the harness falls back to running Steps 2
    and 3 only (equalization + blur) and prints a warning. This fallback is
    for development convenience only — undistortion is required in production.

    Directory structure:
        vision_stack/sample_img/duckietown/s1/
        vision_stack/sample_img/duckietown/s2/
        vision_stack/sample_img/duckietown/s3/
        vision_stack/sample_img/duckietown/s4/
        vision_stack/sample_img/duckietown/s5/
    """
    import os
    import sys

    SAMPLE_DIRS = [
        "vision_stack/sample_img/duckietown/s1",
        "vision_stack/sample_img/duckietown/s2",
        "vision_stack/sample_img/duckietown/s3",
        "vision_stack/sample_img/duckietown/s4",
        "vision_stack/sample_img/duckietown/s5",
    ]
    CALIBRATION_PATH = "vision_stack/calibration/camera_matrix.npz"
    GAUSSIAN_KERNEL  = (5, 5)
    GAUSSIAN_SIGMA   = 0.0
    IMAGE_EXTENSIONS = (".jpg", ".jpeg", ".png", ".bmp")

    # --- Load calibration (or warn and skip undistortion) -------------------
    camera_matrix = None
    dist_coeffs   = None
    skip_undistort = False

    if os.path.exists(CALIBRATION_PATH):
        try:
            camera_matrix, dist_coeffs = load_calibration(CALIBRATION_PATH)
            print(f"[INFO] Calibration loaded from {CALIBRATION_PATH}")
        except KeyError as e:
            print(f"[ERROR] Calibration file missing expected key: {e}")
            sys.exit(1)
    else:
        print(
            f"[WARNING] {CALIBRATION_PATH} not found. "
            "Undistortion will be SKIPPED. "
            "Run intrinsic calibration before production use."
        )
        skip_undistort = True

    # --- Process each sample directory --------------------------------------
    total_processed = 0
    total_failed    = 0

    for sample_dir in SAMPLE_DIRS:
        if not os.path.isdir(sample_dir):
            print(f"[SKIP] Directory not found: {sample_dir}")
            continue

        results_dir = os.path.join(sample_dir, "results")
        os.makedirs(results_dir, exist_ok=True)

        image_files = sorted(
            f for f in os.listdir(sample_dir)
            if os.path.splitext(f)[1].lower() in IMAGE_EXTENSIONS
        )

        if not image_files:
            print(f"[SKIP] No images found in {sample_dir}")
            continue

        for filename in image_files:
            img_path = os.path.join(sample_dir, filename)
            stem     = os.path.splitext(filename)[0]

            # Load
            original = cv2.imread(img_path)
            if original is None:
                print(f"[FAIL] Could not read: {img_path}")
                total_failed += 1
                continue

            # --- Debug image 1: original ------------------------------------
            cv2.imwrite(os.path.join(results_dir, f"{stem}_1_original.png"), original)

            # --- Step 1: Undistortion (or passthrough if no calibration) ----
            if skip_undistort:
                undistorted = original.copy()
            else:
                undistorted = cv2.undistort(original, camera_matrix, dist_coeffs)

            # --- Debug image 2: undistorted ---------------------------------
            cv2.imwrite(os.path.join(results_dir, f"{stem}_2_undistorted.png"), undistorted)

            # --- Step 2: Histogram equalization -----------------------------
            ycrcb = cv2.cvtColor(undistorted, cv2.COLOR_BGR2YCrCb)
            ycrcb[:, :, 0] = cv2.equalizeHist(ycrcb[:, :, 0])
            equalized = cv2.cvtColor(ycrcb, cv2.COLOR_YCrCb2BGR)

            # --- Debug image 3: equalized -----------------------------------
            cv2.imwrite(os.path.join(results_dir, f"{stem}_3_equalized.png"), equalized)

            # --- Step 3: Gaussian blur --------------------------------------
            blurred = cv2.GaussianBlur(equalized, GAUSSIAN_KERNEL, GAUSSIAN_SIGMA)

            # --- Debug image 4: blurred (final output) ----------------------
            cv2.imwrite(os.path.join(results_dir, f"{stem}_4_blurred.png"), blurred)

            print(f"[OK] {img_path}  →  {results_dir}/{stem}_*.png")
            total_processed += 1

    print(f"\nDone. {total_processed} processed, {total_failed} failed.")
    if skip_undistort:
        print("REMINDER: undistortion was skipped. Provide calibration/camera_matrix.npz for full output.")